export const generateMessage = (
  mode: string,
  cards: CardType[],
  question?: string
): string => {
  if (mode === 'yes_no') {
    const card = cards[0];
    return `
      🧠 Mode: YES/NO

      📌 คำถามจากผู้ใช้:
      "${question}"

      🔮 ไพ่ที่หยิบได้:
      - ${card.name} (upright): ${card.meaning_upright}

      โปรดวิเคราะห์ความหมายของไพ่ใบนี้อย่างละเอียด พร้อมเชื่อมโยงกับคำถามด้านบน และให้คำตอบว่า "ใช่" หรือ "ไม่ใช่" อย่างรอบคอบที่สุดในรูปแบบ JSON ตามโครงสร้างที่กำหนดไว้
      `;
  }

  if (mode === 'daily_life') {
    const [card1, card2, card3] = cards;
    const userIntentMessage = (question && question.trim() !== '')
      ? `📌 ผู้ใช้มีคำถามที่เจาะจงว่า: "${question}" \n กรุณาวิเคราะห์ไพ่ทั้งหมดเพื่อมุ่งเน้นการตอบคำถามนี้เป็นหลัก`
      : `📌 ผู้ใช้ต้องการดูดวงชีวิตทั่วไปประจำวันนี้ โดยไม่มีคำถามเจาะจง`;

    return `
      ${userIntentMessage}

      📌 ผู้ใช้ต้องการดูดวงชีวิตทั่วไป โดยใช้ไพ่ 3 ใบ:

      - 🕰 ไพ่ใบที่ 1 (Past): ${card1.name} (upright): ${card1.meaning_upright}
      - 📍 ไพ่ใบที่ 2 (Present): ${card2.name} (upright): ${card2.meaning_upright}
      - 🔮 ไพ่ใบที่ 3 (Future): ${card3.name} (upright): ${card3.meaning_upright}

      กรุณาวิเคราะห์ไพ่ทั้ง 3 ใบให้เป็นเรื่องราวเดียวกันอย่างลึกซึ้ง พร้อมสรุปภาพรวมดวงชะตา และแนะนำหุ้นที่เหมาะสมกับแนวโน้มในปัจจุบัน (3-5 ตัว) โดยใช้เหตุผลเชิงเทคนิคและปัจจัยพื้นฐาน

      **ตอบกลับในรูปแบบ JSON ตามโครงสร้างที่ระบบกำหนด**
      `;
  }
  if(mode === 'chat_ai'){
    return `
      🧠 Mode: CHAT_AI

      📌 คำถามจากผู้ใช้:
      "${question}"
      `;
  }
  return 'กรุณาเลือกโหมดการทำนายก่อน';
};

